public class Room {
    private Integer room;
    private double weight;

    // Getters
    public Integer getRoom() {
        return room;
    }
    public double getWeight() {
        return weight;
    }

    // Setters
    public void setRoom(Integer newroom) {
        this.room = newroom;
    }
    public void setWeight(double newweight) {
        this.weight = newweight;
    }
}
